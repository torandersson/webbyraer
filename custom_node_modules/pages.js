var getIds = function(property){
	var values = [],
		pages = getList();

	for (var i = pages.length - 1; i >= 0; i--) {
		
		if(pages[i][property] && pages[i][property] != '')
			values.push(pages[i][property]);
	};

	return values;

}

var getAsHash = function(){
	var hash = {};

	pages = getList();

	for (var i = pages.length - 1; i >= 0; i--) {
		if(pages[i].twitterid && pages[i].twitterid != '')
			hash[pages[i].twitterid.toLowerCase()] = pages[i];
		if(pages[i].facebookid && pages[i].facebookid != '')
			hash[pages[i].facebookid] = pages[i];

		hash[pages[i].name.toLowerCase()] = pages[i];

	};

	return hash;

}

var getByName = function(name) {
	var dict = getAsHash();
	return dict[name];
}

